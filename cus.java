    private boolean validatePayment() {
    String cardNumber = CardNumberField.getText();
    String expirationDate = ExpirationDateField.getText();
    String cvv = CVVField.getText();

    if (!cardNumber.matches("\\d{16}")) {
        JOptionPane.showMessageDialog(null, "Invalid card number. Must be 16 digits.", "Invalid Input", JOptionPane.ERROR_MESSAGE);
        return false;
    }

    // Validate expiration date
    try {
        YearMonth expiration = YearMonth.parse(expirationDate, DateTimeFormatter.ofPattern("MM/yy"));
        if (expiration.isBefore(YearMonth.now())) {
            JOptionPane.showMessageDialog(null, "Card has expired.", "Invalid Input", JOptionPane.ERROR_MESSAGE);
            return false;
        }
    } catch (DateTimeParseException ex) {
        JOptionPane.showMessageDialog(null, "Invalid expiration date. Use format MM/YY.", "Invalid Input", JOptionPane.ERROR_MESSAGE);
        return false;
    }

    // Validate CVV
    if (!cvv.matches("\\d{3}")) {
        JOptionPane.showMessageDialog(null, "Invalid CVV. Must be 3 digits.", "Invalid Input", JOptionPane.ERROR_MESSAGE);
        return false;
    }

    return true;  // Return true if all validations pass
} 
    
    public void savePanelAsPDF(JPanel panel, String filePath) {
        // Hide buttons before export
        ExportAsPDFButton.setVisible(false);
        DoneButton.setVisible(false);

        try {
            // Create a BufferedImage to draw the panel
            BufferedImage image = new BufferedImage(panel.getWidth(), panel.getHeight(), BufferedImage.TYPE_INT_ARGB);
            Graphics2D g2d = image.createGraphics();
            panel.paint(g2d);
            g2d.dispose();

            // Create a new PDF document
            PDDocument document = new PDDocument();
            PDPage page = new PDPage(new PDRectangle(panel.getWidth(), panel.getHeight()));
            document.addPage(page);

            // Create PDImageXObject from BufferedImage
            PDImageXObject pdImage = LosslessFactory.createFromImage(document, image);

            // Create a new content stream and draw the image
            PDPageContentStream contentStream = new PDPageContentStream(document, page);
            contentStream.drawImage(pdImage, 0, 0);
            contentStream.close();

            // Save the document
            document.save(new File(filePath));
            document.close();

            JOptionPane.showMessageDialog(null, "PDF saved successfully!");
        } catch (IOException e) {
            e.printStackTrace();
            JOptionPane.showMessageDialog(null, "Error saving PDF: " + e.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
        } finally {
            // Show buttons again after export
            ExportAsPDFButton.setVisible(true);
            DoneButton.setVisible(true);
        }
    }
   
