    private void Booking_ButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_Booking_ButtonActionPerformed
        String hallid = hallIdCombo.getSelectedItem().toString();
        String Remarks = remarksArea.getText();
        String CuID = getCuID();

        if (hallid.isEmpty() || Remarks.isEmpty()) {
            JOptionPane.showMessageDialog(null, "Fields cannot be empty", "Validation Error", JOptionPane.ERROR_MESSAGE);
        } else {
            LocalDate start_Date = getDateFromDateChooser(startDateChooser);
            LocalDate end_Date = getDateFromDateChooser(endDateChooser);
            String Start_Time = Start_Booking_Time.getSelectedItem().toString();
            String End_Time = End_Booking_Time.getSelectedItem().toString();
            DateTimeFormatter timeFormatter = DateTimeFormatter.ofPattern("hh:mm a");

            try {
                LocalTime Start_Time_B = LocalTime.parse(Start_Time, timeFormatter);
                LocalTime End_Time_B = LocalTime.parse(End_Time, timeFormatter);
                LocalDateTime start_Date_Time = LocalDateTime.of(start_Date, Start_Time_B);
                LocalDateTime end_Date_Time = LocalDateTime.of(end_Date, End_Time_B);

                Booking book = new Booking(null,null,null,0,0,null,null,null,null,null,null,null);

                // Store the initial booking ID (should be empty or null)
                String initialBookingID = BookingIDLabel.getText();

                // Call bookHall method
                book.bookHall(hallid, start_Date_Time, end_Date_Time, Remarks, CuID, INVStartDateLabel, INVEndDateLabel, INVHallTypeLabel, BookingIDLabel, ReceiptDescLabel, ReceiptAmountLabel, ReceiptTotalLabel);

                // Check if the booking ID has changed, indicating a successful booking
                if (!BookingIDLabel.getText().equals(initialBookingID) && !BookingIDLabel.getText().isEmpty()) {
                    // Booking was successful
                    String bookingID = BookingIDLabel.getText();
                    String hallType = INVHallTypeLabel.getText();
                    double amount = Double.parseDouble(ReceiptTotalLabel.getText().replace("RM", "").trim());

                    // Show PaymentPanel
                    BookPanel.setVisible(false);
                    PaymentPanel.setVisible(true);

                    // Set the amount in the PayAmount text field
                    PayAmount.setText(String.format("RM %.2f", amount));

                    // Set today's date in ReceiptDateLabel
                    LocalDate today = LocalDate.now();
                    DateTimeFormatter dateFormatter = DateTimeFormatter.ofPattern("yyyy-MM-dd");
                    ReceiptDateLabel.setText(today.format(dateFormatter));
                } else {
                    // Booking was unsuccessful
                    JOptionPane.showMessageDialog(null, "Booking was unsuccessful. Please check your inputs and try again.", "Booking Error", JOptionPane.ERROR_MESSAGE);
                    // User remains on the BookPanel
                }
            } catch (Exception ex) {
                // If an exception occurs, assume booking was unsuccessful
                JOptionPane.showMessageDialog(null, "Booking was unsuccessful: " + ex.getMessage(), "Booking Error", JOptionPane.ERROR_MESSAGE);
                // User remains on the BookPanel
            }
        }
    }//GEN-LAST:event_Booking_ButtonActionPerformed

