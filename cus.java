    public Customer getCustomerInfo(String id, String email, String password) {
        try (BufferedReader reader = new BufferedReader(new FileReader("src\\database\\Customer.txt"))) {
            String line;
            while ((line = reader.readLine()) != null) {
                String[] parts = line.split(", ");
                if (parts[0].equals(id) && parts[3].equals(email) && parts[5].equals(password)) {
                    
                    Customer customer = new Customer(null,null,null,null,null,null,null);

                    customer.setCustomerID(parts[0]);
                    customer.setFirstName(parts[1]);
                    customer.setLastName(parts[2]);
                    customer.setEmail(parts[3]);
                    customer.setPassword(parts[5]);
                    customer.setAddress(parts[6]);
                    customer.setPhoneNumber(parts[7]);
                    return customer;
                    
                }
            }
        } catch (IOException e) {
            System.out.println("Error reading the file: " + e.getMessage());
        }
        return null;
    }
    
