    private void LogInButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_LogInButtonActionPerformed
        String email = EmailLogin.getText().trim();
        String id = IDLogin.getText().trim();
        cus cusGUI = new cus(null,null);
        cusGUI.setCuID(id);
        String password = PasswordLogin.getText().trim();
        cusGUI.setCuPass(password);
        Customer_Login customerLogin = new Customer_Login();
        boolean isLoginSuccessful = customerLogin.cus_log(email, id, password);

        if (isLoginSuccessful) {
            // Retrieve customer information
            Customer customer = cusGUI.getCustomerInfo(id, email, password);
            
            if (customer != null) {
                // Assuming getCustomerInfo populates these fields
                String currentCustomerId = customer.getCustomerID();
                cusGUI.ShowCustomerID.setText(currentCustomerId);
                cusGUI.ShowCustomerFirstName.setText(customer.getFirstName());
                cusGUI.ShowCustomerLastName.setText(customer.getLastName());
                cusGUI.ShowCustomerEmail.setText(customer.getEmail());
                cusGUI.ShowCustomerMobileNumber.setText(customer.getPhoneNumber());
                cusGUI.ShowCustomerAddress.setText(customer.getAddress());

                // Create Customer object with updated information

                cusGUI.displayCustomerProfile(customer); // Show customer profile
                new Main().setVisible(false);
                this.dispose();
                cusGUI.setVisible(true);
            } else {
                JOptionPane.showMessageDialog(this, "Error retrieving customer information", "Error", JOptionPane.ERROR_MESSAGE);
            }
        } 

        // Clear the input fields

    }//GEN-LAST:event_LogInButtonActionPerformed

