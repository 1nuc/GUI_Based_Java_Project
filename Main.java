    private void SignUpButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_SignUpButtonActionPerformed
        Customer customerRegister = new Customer(null,null,null,null,null,null,null);

        String firstName = GetFirstName.getText().trim();
        String lastName = GetLastName.getText().trim();
        String email = GetEmail.getText().trim();
        String password = GetPassword.getText().trim();
        String address = GetAddress.getText().trim();
        String phoneNumber = GetPhoneNumber.getText().trim();

        String customerId = customerRegister.registerCustomer(firstName, lastName, email, password, address, phoneNumber);

        if (customerId != null) {
            // Clear the fields
            GetFirstName.setText("");
            GetLastName.setText("");
            GetEmail.setText("");
            GetPassword.setText("");
            GetAddress.setText("");
            GetPhoneNumber.setText("");

            // Display customer profile
            cus cusGUI = new cus(null,null);
            cusGUI.setCuID(customerRegister.getCustomerID());
            cusGUI.setCuPass(customerRegister.getPassword());
            cusGUI.displayCustomerProfile(customerRegister);
            // Switch to customer profile panel
            new Main().setVisible(false);
            this.dispose();
            cusGUI.setVisible(true);
            // Hide other panels if necessary
        }
    }//GEN-LAST:event_SignUpButtonActionPerformed

